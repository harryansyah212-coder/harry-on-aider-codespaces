GOOGLE_API_KEY=your-google-key-here
EOF

echo -e "${YELLOW}⚠️  Jangan lupa isi API keys di ~/.harry-on/.env${NC}"

# ============================================================
# STEP 6: Finalize
# ============================================================
echo -e "${BLUE}[6/6]${NC} Finalizing..."

# Reload PATH
export PATH="$HOME/.harry-on:$PATH"

echo ""
echo "╔══════════════════════════════════════════════════════════╗"
echo "║     🎉 HARRY ON Aider Setup COMPLETE!                    ║"
echo "╚══════════════════════════════════════════════════════════╝"
echo ""
echo -e "${GREEN}✅ Aider installed${NC}"
echo -e "${GREEN}✅ HARRY ON commands ready${NC}"
echo -e "${GREEN}✅ VS Code extensions ready${NC}"
echo ""
echo "📚 Commands:"
echo "   harry-aider    — Launch Aider with AI rotation"
echo "   harry-start    — Create new project"
echo "   harry-resume   — Resume from checkpoint"
echo "   harry-status   — Show status"
echo ""
echo "⚡ Next Steps:"
echo "   1. Isi API keys: cp ~/.harry-on/.env.template ~/.harry-on/.env"
echo "   2. Edit file: nano ~/.harry-on/.env"
echo "   3. Source: source ~/.bashrc"
echo "   4. Start: harry-start my-project web"
echo ""
