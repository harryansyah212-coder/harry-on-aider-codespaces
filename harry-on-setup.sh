  - "harry-gpt: gpt-5.5-pro"
  - "harry-claude: claude-opus-4"
  - "harry-kimi: kimi-k2-6"
  - "harry-gemini: gemini-1.5-pro"

lint_cmds:
  python: "python -m py_compile"
  javascript: "node --check"
  typescript: "tsc --noEmit"

test_cmds:
  python: "python -m pytest"
  javascript: "npm test"
EOF

echo -e "${GREEN}✅ Aider configured${NC}"

# ============================================================
# STEP 5: Setup Environment Variables Template
# ============================================================
echo -e "${BLUE}[5/6]${NC} Setting up environment..."

cat > "$HOME/.harry-on/.env.template" << 'EOF'
# HARRY ON — API Keys
# Copy ke .env dan isi dengan API keys baginda

